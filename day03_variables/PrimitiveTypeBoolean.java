package day03_variables;

public class PrimitiveTypeBoolean {
	public static void main(String[] args) {
		
		boolean result= true;
		
		boolean isMondayToday = false;
		
		boolean isTimeForBreak = false;
		
		System.out.println(result); // true
		
		System.out.println(isMondayToday);
		System.out.println(isMondayToday);
		System.out.println(isMondayToday);
		System.out.println(isMondayToday);
		System.out.println(isMondayToday);
		
		
		System.out.println(isTimeForBreak);
		
		
		System.out.println("result="+result); 
		System.out.println("isMondayToday = "+isMondayToday);
		System.out.println("isTimeForBreak = "+isTimeForBreak);
		
		
		
		
	}
}
